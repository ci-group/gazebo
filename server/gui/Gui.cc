#include "Gui.hh"

using namespace gazebo;

////////////////////////////////////////////////////////////////////////////////
///Constructor
Gui::Gui()
{
  this->windowId = 0;
  this->visual = NULL;
  this->display = NULL;
}

////////////////////////////////////////////////////////////////////////////////
/// Destructor
Gui::~Gui()
{
}
////////////////////////////////////////////////////////////////////////////////
/// Get the id of the window
Window Gui::GetWindowId() const
{
  return this->windowId;
}       

////////////////////////////////////////////////////////////////////////////////
/// Get the visual info
XVisualInfo *Gui::GetVisualInfo() const
{
  return this->visual;
}

////////////////////////////////////////////////////////////////////////////////
/// Get the display
Display *Gui::GetDisplay() const
{
  return this->display;
}
